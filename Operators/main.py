# The '+' is used to add two numbers
1 + 3

# The '-' is used to subject two numbers 
3 - 1

# The '*' is used to multiply two numbers
2 * 2

# The '**' is used to create an exponent or show the power of a number
5 ** 2

# The '/' is used to divide two numbers and keep the remainder
10 / 3

# The '//' is used to divide two numbers and discard the remainder
10 // 3

# The '%' is called modulus and is used to only keep the remainder
10 % 3

# A boolean value is either True or False
True
False

# The '==' is used to test if two values are equal
1 == 1
1 == 2

# The '!=' is used to test if two values are not equal
1 != 1
1 != 2

# The '<' is used to test if the first value is less than the second value
1 < 1
1 < 2

# The '>' is used to test if the first value is greater than the second value
1 > 1
2 > 1

# The '<=' is used to test if the first value is less than or equal to the second value
1 <= 1
1 <= 2

# The '>=' is used to test if the first value is greater than or equal to the second value
1 >= 1
1 >= 2

# The boolean operators 'and', 'or', 'not' can be used to create complex statements
1 or 0
1 and 0
not 1

# Multiple values can be combined but the order is important.  Can be changed with parenthesis '()'
#
# Order:
#   *, /, //, %
#   +, -
#   <, <=, >, >=, !=, ==
1 + 1 + 1 + 1
1 + 2 * 3
(1 + 2) * 3

